"""
Abs, Len, Sum e Round

abs() - É como se fosse o módulo de um valor, retorna o valor absoluto.
len() - Retorna o tamanho do iterável, pode ser lista, tupla, dict, string, etc.
sum() - Retorna a soma de determinado iterável, lista, tupla, etc.
round() - Arredonda um número para x casas após a vírgula.
"""

print(abs(-1))
print(len('Carro'))
print(sum(range(1,11)))
print(round(1.565745635,2))